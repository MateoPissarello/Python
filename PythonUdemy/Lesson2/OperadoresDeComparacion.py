a = 4
b = 6

# ¿a es igual a b?
resultado = (a == b)
print(f"Resultado == : {resultado}")

# ¿a es distinto a b?
resultado = (a != b)
print(f"Resultado != : {resultado}")

# ¿a es mayor a b?
resultado = (a > b)
print(f"Resultado > : {resultado}")

# ¿a es mayor o igual a b?
resultado = (a >= b)
print(f"Resultado >= : {resultado}")

# ¿a es menor que b?
resultado = (a < b)
print(f"Resultado < : {resultado}")

# ¿a es menor o igual que b?
resultado = (a <= b)
print(f"Resultado <= : {resultado}")