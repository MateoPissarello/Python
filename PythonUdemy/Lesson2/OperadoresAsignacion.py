miVariable = 10
print(miVariable)

# Forma no tan reducida de realizar cualquiera de las operaciones de abajo
miVariable = miVariable + 1
print(miVariable)

# Incremento con reasignacion
miVariable += 1
print(miVariable)

# Resta con reasignacion
miVariable -= 2
print(miVariable)

# Multiplicacion con reasignacion
miVariable *= 3
print(miVariable)

# Division con reasignacion
miVariable /= 2
print(miVariable)