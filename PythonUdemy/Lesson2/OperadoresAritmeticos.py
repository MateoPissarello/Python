operandoA = 3
operandoB = 2

# Suma
suma = operandoA + operandoB
#print("Resultado suma:", suma)
print(f"Resultado suma: {suma}")

# Resta
resta = operandoA - operandoB
print(f"Resultado de resta: {resta}")

# Multiplicación
multiplicacion = operandoA * operandoB
print(f"Resultado de la multiplicación: {multiplicacion}")

# Division
division = operandoA / operandoB
print(f"Resultado de la division: {division}")

# Division entera
divisionEntera = operandoA // operandoB
print(f"Resultado division entera: {divisionEntera}")

# Residuo o modulo
residuo = operandoA % operandoB
print(f"Resultado de residuo de la división(modulo): {residuo}")

# Exponente
exponente = operandoA ** operandoB
print(f"Resultado del exponente: {exponente}")